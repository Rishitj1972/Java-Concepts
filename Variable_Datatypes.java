class Variable_Datatypes {

    public static void main(String[] args) {

        // Variable Declarations and Data Types in Java.

        // Integer data type 4 bytes
        int myNum = 5;
        System.out.println("Integer: " + myNum);

        // Short data type  2 bytes
        short myShortNum = 5000;
        System.out.println("Short: " + myShortNum);

        // Long data type 8 bytes
        long myLongNum = 15000000000L;
        System.out.println("Long: " + myLongNum);

        // Byte data type 1 byte
        byte myByteNum = 100;
        System.out.println("Byte: " + myByteNum);

        // Floating point data type 4 bytes
        float myFloatNum = 5.99f;
        System.out.println("Float: " + myFloatNum);

        // Double data type 8 bytes
        double myDoubleNum = 19.99d;
        System.out.println("Double: " + myDoubleNum);

        // Character data type 2 bytes
        char myLetter = 'D';
        System.out.println("Character: " + myLetter);

        // String data type 
        String myText = "Hello";
        System.out.println("String: " + myText);

        // Boolean data type 1 bit
        boolean myBool = true;
        System.out.println("Boolean: " + myBool);
    }
}